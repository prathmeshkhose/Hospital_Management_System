public void Channel_table() {
        try {
            ps = con.prepareStatement("SELECT channel.channelno, doctor.name AS doctor_name, patient.name AS patient_name, channel.roomno, channel.date\n" +
"FROM channel\n" +
"INNER JOIN doctor ON channel.doctorname = doctor.doctorno\n" +
"INNER JOIN patient ON channel.patientname = patient.patientno\n" +
"WHERE doctor.log_id = ?");

         ps.setInt(1, newid);
            rs = ps.executeQuery();
            ResultSetMetaData Ram = rs.getMetaData();
            int c;
            c = Ram.getColumnCount();
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {

                    v2.add(rs.getString(1));
                    v2.add(rs.getString(2));
                    v2.add(rs.getString(3));
                    v2.add(rs.getString(4));
                    v2.add(rs.getString(5));
                }
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Channel.Patient.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
